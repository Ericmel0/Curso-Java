public class Variáveis {
    public static void main(String[] args) {
        int idade = 10;
        double num = 10.5;
        boolean teste = true;
        String nome = "Maria";
        System.out.println("" + idade + " " + num + " " + teste + " " + nome);
        int a = 2;
        int b = 3;
        int c = 2 + 3;
        System.out.println("" + a + ", " + b + ", " + c);
    }
}
